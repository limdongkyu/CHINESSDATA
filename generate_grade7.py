def build_categories(data):
    """카테고리 분류 (의미별 그룹)"""
    categories = {
        "숫자": [],      # 一二三四五六七八九十 百千萬萬
        "자연": [],      # 日月火水木金土 山川江河海天地 花草春夏秋冬
        "사람/가족": [], # 人父母子女男女兄弟
        "방향/위치": [], # 東西南北上下左右中
        "학교/학문": [], # 學校教
        "생활/일상": [], # 食休安住場市
        "기타": []
    }
    
    char_meanings = {
        "숫자": ["一", "二", "三", "四", "五", "六", "七", "八", "九", "十", "百", "千", "萬"],
        "자연": ["日", "月", "火", "水", "木", "金", "土", "山", "川", "江", "海", "天", "地", "花", "草", "春", "夏", "秋", "冬"],
        "사람/가족": ["人", "父", "母", "子", "女", "男", "兄", "弟", "祖", "孝"],
        "방향/위치": ["東", "西", "南", "北", "上", "下", "左", "右", "中", "內", "外", "前", "後"],
        "학교/학문": ["學", "校", "敎", "文", "字", "問", "記", "算", "數"],
        "생활/일상": ["食", "休", "安", "住", "場", "市", "工", "農", "電", "家", "室", "門", "道", "路", "里", "村", "洞"],
    }
    
    char_to_idx = {e["character"]: i for i, e in enumerate(data)}
    for cat, chars in char_meanings.items():
        for c in chars:
            if c in char_to_idx:
                idx = char_to_idx[c]
                if idx not in categories.get(cat, []):
                    if cat not in categories:
                        categories[cat] = []
                    categories[cat].append(idx)
    
    # 나머지는 기타
    all_categorized = set()
    for indices in categories.values():
        all_categorized.update(indices)
    categories["기타"] = [i for i in range(len(data)) if i not in all_categorized]
    categories["기타"].sort()
    
    return {k: sorted(v) for k, v in categories.items() if v}


def main():
    data = []
    for char, meaning, sound, stroke_count, words in GRADE7_DATA:
        entry = {
            "character": char,
            "meaning": meaning,
            "sound": sound,
            "strokeCount": stroke_count,
            "words": [
                {"hanja": w[0], "reading": w[1], "meaning": w[2]}
                for w in words
            ]
        }
        data.append(entry)
    
    categories = build_categories(data)
    
    result = {
        "version": "0.5",
        "data": data,
        "categories": categories
    }
    
    with open("default7.json", "w", encoding="utf-8") as f:
        json.dump(result, f, ensure_ascii=False, indent=2)
    
    print(f"default7.json 생성 완료: {len(data)}자")
    print(f"카테고리: {list(categories.keys())}")


